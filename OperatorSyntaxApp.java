class OperatorSyntaxApp {
        
        public static void main(String[] args) {
            int x = 5;
            int y = 15;

            x+=y;                           // x = x + y, 20 = 5 + 15
            boolean check = x >= y;         // holds the result on boolean, if 20 >= 15
            System.out.println(check);
        }
}
